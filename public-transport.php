<p>Zagreb offers a well-organized and efficient public transportation system, providing residents and visitors with an easy way to get around the city. The city's transportation network includes trams, buses, and trains, all of which are operated by the Zagrebački električni tramvaj (ZET). Whether you’re exploring the city center or traveling to its outskirts, Zagreb’s public transport is both affordable and convenient.</p>

<h2>Trams</h2>
<img src="img/tram.jpg" width="300" alt="tram">
<p>Trams are one of the most popular modes of transportation in Zagreb, and they cover a significant part of the city. The tram system consists of 15 lines, each serving different neighborhoods, from the central area to the suburbs. Trams are frequent, with most lines running every 5-10 minutes during peak hours.</p>
<ul>
    <li><span class="bold">Operating Hours</span>: Trams run from 4:00 AM to midnight. After midnight, a limited number of night trams operate on certain routes.</li>
    <li><span class="bold">Ticketing</span>: Tickets can be purchased via the ZET mobile app, from ticket machines, or on board from tram conductors. Remember to validate your ticket before boarding.</li>
</ul>

<h2>Buses</h2>
<img src="img/bus.jpg" width="300" alt="bus">
<p>In addition to trams, Zagreb’s bus network is an essential part of the city’s public transport system. The buses are especially useful for areas not served by trams, including the outskirts of the city and specific residential neighborhoods.</p>
<ul>
    <li><span class="bold">Bus Lines</span>: There are more than 100 bus lines connecting different parts of Zagreb. Some buses operate on a regular timetable, while others, like the express buses, are designed to serve longer routes more efficiently.</li>
    <li><span class="bold">Operating Hours</span>: Similar to trams, buses run from early morning until midnight. Night buses are also available for certain routes.</li>
    <li><span class="bold">Tickets</span>: Tickets can be purchased on board or via the ZET mobile app. Like trams, tickets need to be validated.</li>
</ul>

<h2>Trains</h2>
<p>For those planning to visit nearby towns or cities, Zagreb’s train network is a great option. The main railway station, Zagreb Glavni Kolodvor, is centrally located and well-connected to both local and national routes.</p>
<ul>
    <li><span class="bold">Suburban Trains</span>: Suburban trains are available for travel between Zagreb and surrounding towns, such as Samobor, Velika Gorica, and Sesvete. These trains are operated by HŽ Putnički prijevoz (Croatian Railways).</li>
    <li><span class="bold">Timetables</span>: Trains run regularly throughout the day, with departures at different intervals depending on the route.</li>
    <li><span class="bold">Tickets</span>: Tickets can be purchased at the station, online, or via the mobile app.</li>
</ul>

<h2>Tickets and Fares</h2>
<p>Zagreb’s public transport system operates on a single ticket system, meaning one ticket is valid for both trams and buses. There are also time-based tickets, such as:</p>
<ul>
    <li><span class="bold">Single Ticket</span>: Valid for a 30-minute ride.</li>
    <li><span class="bold">1-Hour Ticket</span>: Allows unlimited travel within one hour of validation.</li>
    <li><span class="bold">Day Pass</span>: Ideal for tourists, this allows unlimited travel for a full day.</li>
</ul>

